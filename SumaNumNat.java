package practica_Java_1;

public class SumaNumNat {

	public static void main(String[] args) {
		
		int suma_cien = 0;

		System.out.println("Suma de los primeros 100 números naturales");

		for(int i = 1; i <= 100; i++) {
			
			suma_cien += i;
				
			}
			
		System.out.println("El resultado de la suma de todos los números es "+suma_cien);

	}

  }


	

