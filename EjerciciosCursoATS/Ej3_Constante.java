package programacion_ats;

public class Ej3_Constante {

	public static void main(String[] args) {
		
		// Los tipos de dato no primitivo son:
		// Boolean
		// Integer
		// Double
		// String
		// Estos tipos de dato se diferencian  de los primitivos en que pueden implementar metodos
		// al finalizar con un punto "."
		
		// Las constantes son datos almacenados que no pueden cambiar
		// Este contenedor se define como variable escribiendo la palabra "final" al principio
		
		final int numero = 10;
		
		System.out.print("Este valor es una constante: " + numero);

	}

}
