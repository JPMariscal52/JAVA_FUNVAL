package programacion_ats;

public class Ej19_ForLoop {

	public static void main(String[] args) {
		
		//En el ciclo for se establece primero el numero de iteraciones y despues se ejecuta
		
		int numero = 10;
		
		//for(inicializacion; condicion; aumento o decremento)
		for(int i = 0; i <= numero; i+=2) {
			
			System.out.println(i);
			
		}

	}

}
