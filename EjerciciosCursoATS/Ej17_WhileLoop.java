package programacion_ats;

public class Ej17_WhileLoop {

	public static void main(String[] args) {
		
		//En el ciclo while primero se evalua la condición y luego se ejecuta, de ser falsa
		//la condicion, el ciclo nunca se ejecutará.
		int i = 0;
		
		while(i <= 15) {
			
			System.out.println(i);
			
			i++;
		}

	}

}
